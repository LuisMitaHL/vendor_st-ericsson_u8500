AB8500_version Get_AB8500_Version(void)
{
   AB8500_version version = AB8500_VER_UNDEFINED;
  uint8_t data;
	
  Read4500reg (0x10, 0x80,&data);
  switch(data & 0xF0)
  {
      case 0x00:
        version = AB8500_VER_ED;
        break;
        
      case 0x10:
        version = AB8500_VER_V1;
        break;
      case 0x20:
      default:
        version = AB8500_VER_V2;
        break;
  }
	return version;
}
